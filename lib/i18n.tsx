const DICTS: Record<Lang, Tree> = { fr: FR, en: EN };

function lookup(tree: Tree, path: string): string {
  const parts = path.split(".");
  let node: string | Tree = tree;
  for (const p of parts) {
    if (typeof node === "string" || node[p] === undefined) return path;
    node = node[p];
  }
  return typeof node === "string" ? node : path;
}

// ------------------------------------------------------------------
//  Contexte React
// ------------------------------------------------------------------
interface LangContextValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  toggle: () => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  tr: (loc: Localized) => string;
}

const LangContext = createContext<LangContextValue | null>(null);
const STORAGE_KEY = "tc-lang";

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("fr");

  useEffect(() => {
    const stored = window.localStorage.getItem(STORAGE_KEY) as Lang | null;
    if (stored === "fr" || stored === "en") {
      setLangState(stored);
      document.documentElement.lang = stored;
    }
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    window.localStorage.setItem(STORAGE_KEY, l);
    document.documentElement.lang = l;
  };

  const value: LangContextValue = {
    lang,
    setLang,
    toggle: () => setLang(lang === "fr" ? "en" : "fr"),
    t: (key, vars) => {
      let s = lookup(DICTS[lang], key);
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          s = s.replace(`{${k}}`, String(v));
        }
      }
      return s;
    },
    tr: (loc) => loc[lang],
  };

  return <LangContext.Provider value={value}>{children}</LangContext.Provider>;
}

export function useLang(): LangContextValue {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useLang must be used within LanguageProvider");
  return ctx;
}
